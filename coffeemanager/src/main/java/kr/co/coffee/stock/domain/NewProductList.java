package kr.co.coffee.stock.domain;

public class NewProductList {
	private String newpd_CD;
	private String newpd_DATE;
	private String ing_CD;
	private int newpd_CNT;
	public String getNewpd_CD() {
		return newpd_CD;
	}
	public void setNewpd_CD(String newpd_CD) {
		this.newpd_CD = newpd_CD;
	}
	public String getNewpd_DATE() {
		return newpd_DATE;
	}
	public void setNewpd_DATE(String newpd_DATE) {
		this.newpd_DATE = newpd_DATE;
	}
	public String getIng_CD() {
		return ing_CD;
	}
	public void setIng_CD(String ing_CD) {
		this.ing_CD = ing_CD;
	}
	public int getNewpd_CNT() {
		return newpd_CNT;
	}
	public void setNewpd_CNT(int newpd_CNT) {
		this.newpd_CNT = newpd_CNT;
	}
	
	
}
